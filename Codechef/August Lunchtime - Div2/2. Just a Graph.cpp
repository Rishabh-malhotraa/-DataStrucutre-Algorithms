#include <bits/stdc++.h>
using namespace std;

void solve()
{
  int n;
  cin >> n;
  vector<int> arr(n);
  unordered_map<int, vector<int>> cache;
  for (int i = 0; i < n; i++)
  {
    cin >> arr[i];
    cache[arr[i] - i].push_back(i);
  }
  int result = 1;
  for (auto el : cache)
  {
    if (el.second.size() == n)
      result += n - 1;
  }
  cout << result << endl;
  return;
}

int main()
{
  ios_base::sync_with_stdio(false);
  cin.tie(NULL);
#ifndef ONLINE_JUDGE
  freopen("input.txt", "r", stdin);
  freopen("output.txt", "w", stdout);
#endif

  int t;
  cin >> t;
  while (t--)
  {
    solve();
  }
  return 0;
}