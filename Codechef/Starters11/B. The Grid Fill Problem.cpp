#include <bits/stdc++.h>
using namespace std;

void solve()
{
  int n;
  cin >> n;

  vector<vector<int>> dp(n, vector<int>(n, -1));

  if ((n % 2) != 0)
    for (int i = 0; i < n; i++)
      dp[i][i] = 1;

  for (int i = 0; i < n; i++)
  {
    for (int j = 0; j < n; j++)
      cout << dp[i][j] << " ";
    cout << endl;
  }

  return;
}

int main()
{
  ios_base::sync_with_stdio(false);
  cin.tie(NULL);
#ifndef ONLINE_JUDGE
  freopen("input.txt", "r", stdin);
  freopen("output.txt", "w", stdout);
#endif

  int t;
  cin >> t;
  while (t--)
  {
    solve();
  }
  return 0;
}