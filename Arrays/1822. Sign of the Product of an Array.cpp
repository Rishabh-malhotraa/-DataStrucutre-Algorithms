#include <bits/stdc++.h>
using namespace std;

class Solution
{
public:
  int arraySign(vector<int> &nums)
  {
    int count_negatives = 0;

    for (int &el : nums)
    {
      if (el == 0)
        return 0;
      else if (el < 0)
        count_negatives++;
    }

    return (count_negatives & 1) ? -1 : 1;
  }
};