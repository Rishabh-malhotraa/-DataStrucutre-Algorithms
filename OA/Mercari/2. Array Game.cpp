#include <bits/stdc++.h>
using namespace std;

long countMoves(vector<int> numbers)
{
  int n = numbers.size();
  long long minimumValue = *min_element(numbers.begin(), numbers.end());
  long long sum = accumulate(numbers.begin(), numbers.end(), 0LL);

  return (sum - (n * minimumValue));
}