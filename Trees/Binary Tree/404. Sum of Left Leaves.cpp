#include <bits/stdc++.h>
using namespace std;

class TreeNode
{
public:
  int val;
  TreeNode *left;
  TreeNode *right;
  TreeNode() : val(0), left(nullptr), right(nullptr) {}
  TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
  TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
};

// same logic as adding leave just add extra condtion while checking the leaf node to make sure its the left leaf or not

class Solution
{
public:
  int sumOfLeftLeaves(TreeNode *root, int result = 0)
  {
    if (!root)
      return 0;

    if (root->left)
      if (root->left->left == NULL && root->left->right == NULL)
        result += root->left->val;

    result += sumOfLeftLeaves(root->left);
    result += sumOfLeftLeaves(root->right);

    return result;
  }
};
