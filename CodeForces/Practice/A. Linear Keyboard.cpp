#include <bits/stdc++.h>
using namespace std;

void solve()
{
  string order, keystrokes;
  cin >> order >> keystrokes;

  unordered_map<char, int> keys;

  int idx = 0, time = 0;
  for (char ch : order)
    keys[ch] = idx++;

  for (int i = 0; i < keystrokes.size() - 1; i++)
  {
    time += abs(keys[keystrokes[i]] - keys[keystrokes[i + 1]]);
  }

  cout << time << endl;
  return;
}

int main()
{
  ios_base::sync_with_stdio(false);
  cin.tie(NULL);
#ifndef ONLINE_JUDGE
  freopen("input.txt", "r", stdin);
  freopen("output.txt", "w", stdout);
#endif

  int t;
  cin >> t;
  while (t--)
  {
    solve();
  }
  return 0;
}