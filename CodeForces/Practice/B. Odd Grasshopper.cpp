#include <bits/stdc++.h>
using namespace std;

void solve()
{
  long long n, x;
  cin >> x >> n;

  long long jump = ((n / 4) * 4) + 1;
  long long destination = x;
  int time = n - ((n / 4) * 4);

  for (int i = 1; i <= time; i++, jump++)
  {
    int sign = (destination & 1) ? 1 : -1;
    destination = destination + (sign * jump);
  }
  cout << destination << endl;
  return;
}

int main()
{
  ios_base::sync_with_stdio(false);
  cin.tie(NULL);
#ifndef ONLINE_JUDGE
  freopen("input.txt", "r", stdin);
  freopen("output.txt", "w", stdout);
#endif

  int t;
  cin >> t;
  while (t--)
  {
    solve();
  }
  return 0;
}