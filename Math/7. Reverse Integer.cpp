#include <bits/stdc++.h>
using namespace std;

class Solution
{
public:
  int reverse(int x)
  {
    long long rev = 0;
    for (int i = x; i != 0; i /= 10)
      rev = rev * 10 + (i % 10);

    return (rev > INT_MAX || rev < INT_MIN) ? 0 : rev;
  }
};