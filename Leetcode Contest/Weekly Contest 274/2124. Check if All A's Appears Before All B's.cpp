#include <bits/stdc++.h>
using namespace std;

class Solution
{
public:
  bool checkString(string s)
  {
    int n = s.size();
    int idx = 0;
    for (idx = 0; idx < n; idx++)
      if (s[idx] == 'b')
        break;

    for (; idx < n; idx++)
      if (s[idx] == 'a')
        break;

        return idx == n;
  }
};