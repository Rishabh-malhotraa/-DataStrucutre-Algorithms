#include <bits/stdc++.h>
using namespace std;

class Solution
{
public:
  bool asteroidsDestroyed(int mass, vector<int> &asteroids)
  {
    sort(asteroids.begin(), asteroids.end());
    long long wt = mass;
    for (int asteroid : asteroids)
    {
      if (wt < asteroid)
        return false;
      wt += asteroid;
    }
    return true;
  }
};