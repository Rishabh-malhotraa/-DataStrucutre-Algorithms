#include <bits/stdc++.h>
using namespace std;

class Solution
{
public:
  int mostWordsFound(vector<string> &sentences)
  {
    int result = 0;
    for (string &sentence : sentences)
    {
      stringstream ss(sentence);
      string token;
      int count = 0;
      while (getline(ss, token, ' '))
        count++;
      result = max(result, count);
    }
    return result;
  }
};