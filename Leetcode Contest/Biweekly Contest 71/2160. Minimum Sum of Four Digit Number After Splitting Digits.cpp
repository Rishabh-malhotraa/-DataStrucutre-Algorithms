#include <bits/stdc++.h>
using namespace std;

class Solution
{
public:
  int minimumSum(int num)
  {
    string s = to_string(num);

    sort(s.begin(), s.end());
    // cout << s<< endl;
    string s1 = string(1, s[0]) + s[3];
    string s2 = string(1, s[1]) + s[2];

    return stoi(s1) + stoi(s2);
  }
};