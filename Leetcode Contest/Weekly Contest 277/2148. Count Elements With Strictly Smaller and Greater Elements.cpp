#include <bits/stdc++.h>
using namespace std;

class Solution
{
public:
  int countElements(vector<int> &nums)
  {
    sort(nums.begin(), nums.end());
    int minEl = nums.front(), maxEl = nums.back();
    int count = 0;
    for (int el : nums)
      if (el > minEl && el < maxEl)
        count++;
    return count;
  }
};