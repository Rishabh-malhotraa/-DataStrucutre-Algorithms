#include <bits/stdc++.h>
using namespace std;

class Solution
{
public:
  int minNumberOperations(vector<int> &target)
  {
    int previous = 0, ops = 0;
    for (int el : target)
    {
      ops += max(el - previous, 0);
      previous = el;
    }
    return ops;
  }
};