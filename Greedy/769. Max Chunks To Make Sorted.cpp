#include <bits/stdc++.h>
using namespace std;

class Solution
{
public:
  int maxChunksToSorted(vector<int> &arr)
  {
    int chunks = 0, value = 0, n = arr.size();

    for (int i = 0; i < n; i++)
    {
      value = max(value, arr[i]);
      if (value == i)
        chunks++;
    }
    return chunks;
  }
};