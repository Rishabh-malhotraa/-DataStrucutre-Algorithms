#include <bits/stdc++.h>
using namespace std;

class Solution
{
public:
  bool judgeSquareSum(int c)
  {
    int r = ceil(sqrt(c)), l = 0;

    while (l <= r)
    {
      int sum = l * l + r * r;

      if (sum == c)
        return true;
      else if (sum > c)
        r--;
      else
        l++;
    }
    return false;
  }
};