void solve()
{
    int n;
    cin >> n;
    vector<int> arr(n);
    for (int i = 0; i < n; i++)
        cin >> arr[i];

    vector<int> result = getSumAbsoluteDifferences(arr);

    for (int i = 0; i < n; i++)
        cout << result[i] << " ";
    cout << endl;

    int dp[100][100];
    for (int gap = 0; gap < n; gap++)
    {
        for (int i = 0, j = gap; j < n; j++, i++)
        {
            // gap strategy makes sense
            if (gap == 0)
                dp[i][j] = arr[i];
            if (gap == 1)
                dp[i][j] = max(dp[i - 1][j], dp[i][j - 1]);

            else
            {
                int val1 = arr[i] + min(dp[i + 2][j], dp[i + 1][j - 1]);
                int val2 = arr[j] + min(dp[i + 1][j - 1], dp[i][j - 2]);
                dp[i][j] = max(val1, val2);
            }
        }
    }
    return dp[0][n];

    return;
}

